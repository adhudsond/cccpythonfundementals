scores = [95, 87, 92, 78, 88, 91]
# How many scores?
count = len(scores)
print(f'Number of scores: {count}') # 6
# Total points
total = sum(scores)
print(f'Total points: {total}') # 531
# Average score
average = total / count
print(f'Average: {average:.1f}') # 88.5
# Highest and lowest
print(f'Highest: {max(scores)}') # 95
print(f'Lowest: {min(scores)}') # 78
# This is data analysis!