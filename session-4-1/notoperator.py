# Reverse a boolean value
is_raining = False
# If NOT raining, go outside
if not is_raining:
    print('Go for a walk!')
else:
    print('Stay inside')
# not reverses the value:
# not True → False
# not False → True
# Another example
is_member = True
if not is_member:
    print('Please sign up')
# Useful for checking False conditions!