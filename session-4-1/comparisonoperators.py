# Examples with variables
age = 25
score = 95
name = 'Alice'
# Comparisons return True/False
print(age >= 18) # True
print(score == 100) # False
print(name == 'Bob') # False
# Use in if statements
if score >= 90:
    print('A grade!') # Runs!
if age < 21:
    print('Under 21') # Doesn't run
# Common mistake: = vs ==
# = assigns, == compares!
