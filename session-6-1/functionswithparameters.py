# Define function with parameter
def greet(name):
    print(f'Hello, {name}!')
    print('Welcome to Python!')
# Call with different arguments
greet('Alice')
# Hello, Alice!
# Welcome to Python!
greet('Bob')
# Hello, Bob!
# Welcome to Python!
greet('Carol')
# Hello, Carol!
# Welcome to Python!
