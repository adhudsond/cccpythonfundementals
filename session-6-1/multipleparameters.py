# Function with two parameters
def introduce(name, age):
    print(f'My name is {name}')
    print(f'I am {age} years old')
# Call with two arguments
introduce('Alice', 25)
# My name is Alice
# I am 25 years old
introduce('Bob', 30)
# My name is Bob
# I am 30 years old
